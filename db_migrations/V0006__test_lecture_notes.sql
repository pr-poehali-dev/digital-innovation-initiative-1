Лекция 1. Введение в цифровую трансформацию управления проектами
Цифровая трансформация (DX) — это не просто внедрение технологий, а фундаментальное изменение бизнес-процессов, культуры и клиентского опыта.

Ключевые драйверы DX в проектном управлении:
- Удалённые и распределённые команды
- Big Data и предиктивная аналитика
- AI/ML для прогнозирования рисков
- Низкокодовые платформы

Лекция 2. Гибкие методологии в проектах DX
Agile стал стандартом де-факто для цифровых проектов.
Scrum: спринты 2 недели, ретроспективы, демо
Kanban: визуализация потока, лимиты WIP
SAFe: масштабирование Agile на крупные программы
Hybrid (water-scrum-fall): сочетание waterfall и agile

Лекция 3. Цифровые инструменты PM
Системы управления:
- Jira — для разработки
- Asana — для маркетинга
- Monday — универсальная
- Notion — knowledge management

Инструменты коллаборации:
- Miro, FigJam — визуальные доски
- Slack, Teams — коммуникации
- Loom — асинхронное видео

Лекция 4. Управление рисками в DX проектах
Специфические риски:
- Технологический долг
- Сопротивление изменениям
- Кибербезопасность
- Зависимость от вендоров

Методы митигации:
- Технические спайки
- Change Management программы
- Security by design
- Multi-vendor стратегия

Лекция 5. Метрики успеха DX проектов
KPI цифровой трансформации:
- Time to Market (T2M)
- Customer Effort Score (CES)
- Adoption Rate
- ROI цифровых инициатив
- Employee Net Promoter Score

Лекция 6. Лидерство в цифровых проектах
Цифровой лидер должен:
- Думать в категориях экспериментов
- Принимать неопределённость
- Работать с данными
- Вдохновлять команду на изменения
- Балансировать между tech и business

