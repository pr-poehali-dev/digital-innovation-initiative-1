## Проблема

Текущий процесс обработки контрольных замечаний полностью ручной:
- замечания поступают из разных источников без единого формата
- первичный разбор занимает 1–3 дня
- маршрутизация зависит от опыта одного сотрудника
- руководитель не видит актуальный статус без запроса
- подготовка еженедельной сводки — 2–4 часа ручной работы

## Решение

Создать единое цифровое пространство для замечаний:

**Фаза 1 (быстрый старт, без AI):**
- Единый реестр с обязательными полями
- Стандартная карточка замечания
- Автоматическая маршрутизация по правилам
- Dashboard статусов для руководителя

**Фаза 2 (AI-слой):**
- AI-суммаризация входящего текста
- AI-предложение категории (с подтверждением аналитика)
- AI-черновик сводки для руководителя

## Ожидаемый эффект

| Метрика | Сейчас | Цель |
|---|---|---|
| Время первичного triage | 1–3 дня | < 2 часов |
| Доля ручной маршрутизации | 100% | < 20% |
| Время подготовки сводки | 2–4 часа | < 20 минут |
| Прозрачность статусов | через запрос | реальное время |

